    /**
     * Set the image in the panel to correspond to the image for the selected
     * movie in the table.
     * 
     */
    public void setThumbnailImage(Image image) {
        if (image != null) {
            this.coverArtLabel.setIcon(new ImageIcon(image));
        } else {
            this.coverArtLabel.setIcon(new ImageIcon(getClass().getResource("/com/themangoproject/ui/images/defaultMovieImage.png")));
        }
    }
    
    /**
     * List selection listener for movie selections.
     * 
     * @param lse The list selection event.
     */
    public void valueChanged(ListSelectionEvent lse) {
        try {
            JTable table = UIController.getInstance().getViewTable();
            int row = table.getSelectedRow();
            if (row < 0) {
            	return;
            }
            EditableMovieTableModel tm = (EditableMovieTableModel) table.getModel();
            Image i = tm.getMovieForRow(row).getImage();
            setThumbnailImage(i);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

